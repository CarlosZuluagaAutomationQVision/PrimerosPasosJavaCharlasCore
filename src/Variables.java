public class Variables {


    public static void main(String args[]) {

        int edad;
        edad = 15;				//Declaración de Variable en dos lineas

        int a = 10;				//Declaración de Variable en una linea
        short b = 10;
        byte c = 10;
        long d = 10L;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);	//Imprimir los valores de las variables

        System.out.println(edad);

        edad = 30;

        System.out.println("\n" + edad);
    }


}
