public class CalculosUsandoMath {


    public static void main(String[] args) {

        double raiz = 9;
        double calculoRaiz = Math.sqrt(raiz);

        System.out.println("Raiz: " + calculoRaiz);

        double base = 5;
        double exponente = 2;
        double calculoPotencia = Math.pow(base, exponente);


        System.out.println("Potencia: " + calculoPotencia);

    }

}
